const getNode = (lang: Lang, key: string): TranslationNode | undefined => {
  return key.split(".").reduce<TranslationNode | undefined>((acc, part) => {
    if (!acc || typeof acc === "string") return undefined;
    return acc[part];
  }, translations[lang]);
};

const formatMessage = (message: string, vars?: Record<string, string | number>) =>
  message.replace(/\{\{(.*?)\}\}/g, (_, rawKey) => String(vars?.[rawKey.trim()] ?? ""));

const applyLangSettings = (lang: Lang) => {
  const htmlEl = document.documentElement;
  htmlEl.dir = lang === "ar" ? "rtl" : "ltr";
  htmlEl.lang = lang;
  htmlEl.style.fontFamily = lang === "ar" ? "'Tajawal', sans-serif" : "";
};

const I18nContext = createContext<I18nContextType>({
  lang: "en",
  locale: "en-US",
  setLang: () => {},
  dir: "ltr",
  isArabic: false,
  t: (key) => key,
});

export const pickText = <T,>(lang: Lang, arabic: T, english: T) => (lang === "ar" ? arabic : english);

export const I18nProvider = ({ children }: { children: ReactNode }) => {
  const [lang, setLang] = useState<Lang>(() => {
    const saved = localStorage.getItem("lourex-lang");
    return saved === "ar" || saved === "en" ? saved : "en";
  });

  useEffect(() => {
    applyLangSettings(lang);
    localStorage.setItem("lourex-lang", lang);
  }, [lang]);

  const t = useCallback(
    (key: string, vars?: Record<string, string | number>) => {
      const node = getNode(lang, key);
      if (typeof node === "string") return formatMessage(node, vars);
      const fallback = getNode("en", key);
      if (typeof fallback === "string") return formatMessage(fallback, vars);
      return key;
    },
    [lang],
  );

  const value = useMemo<I18nContextType>(
    () => ({
      lang,
      locale: lang === "ar" ? "ar-SA" : "en-US",
      setLang,
      dir: lang === "ar" ? "rtl" : "ltr",
      isArabic: lang === "ar",
      t,
    }),
    [lang, t],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = () => useContext(I18nContext);

export const usePick = () => {
  const { lang } = useI18n();
  return useCallback(<T,>(arabic: T, english: T) => pickText(lang, arabic, english), [lang]);
};
