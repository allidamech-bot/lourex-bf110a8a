const getNode = (lang: Lang, key: string): TranslationNode | undefined =>
  key.split(".").reduce<TranslationNode | undefined>((current, part) => {
    if (!current || typeof current === "string") {
      return undefined;
    }

    return current[part];
  }, translations[lang]);

const formatMessage = (message: string, vars?: Record<string, string | number>) =>
  message.replace(/\{\{(.*?)\}\}/g, (_, rawKey) => String(vars?.[rawKey.trim()] ?? ""));

const humanizeKey = (key: string) => {
  const lastSegment = key.split(".").pop() ?? key;

  return lastSegment
    .replace(/_/g, " ")
    .replace(/([a-z])([A-Z])/g, "$1 $2")
    .replace(/\s+/g, " ")
    .trim()
    .replace(/^\w/, (match) => match.toUpperCase());
};

const resolveTranslation = (lang: Lang, key: string, vars?: Record<string, string | number>) => {
  const direct = getNode(lang, key);
  if (typeof direct === "string") {
    return formatMessage(direct, vars);
  }

  const fallback = getNode("en", key);
  if (typeof fallback === "string") {
    return formatMessage(fallback, vars);
  }

  return humanizeKey(key);
};

const applyLangSettings = (lang: Lang) => {
  const htmlElement = document.documentElement;
  htmlElement.dir = lang === "ar" ? "rtl" : "ltr";
  htmlElement.lang = lang;
  htmlElement.style.fontFamily = lang === "ar" ? "'Tajawal', sans-serif" : "";
};

const I18nContext = createContext<I18nContextType>({
  lang: "en",
  locale: "en-US",
  setLang: () => {},
  dir: "ltr",
  isArabic: false,
  t: (key, vars) => resolveTranslation("en", key, vars),
});

export const pickText = <T,>(lang: Lang, arabic: T, english: T) =>
  lang === "ar" ? arabic : english;

export const I18nProvider = ({ children }: { children: ReactNode }) => {
  const [lang, setLang] = useState<Lang>(() => {
    const saved = localStorage.getItem("lourex-lang");
    return saved === "ar" || saved === "en" ? saved : "en";
  });

  useEffect(() => {
    applyLangSettings(lang);
    localStorage.setItem("lourex-lang", lang);
  }, [lang]);

  const t = useCallback(
    (key: string, vars?: Record<string, string | number>) => resolveTranslation(lang, key, vars),
    [lang],
  );

  const contextValue = useMemo<I18nContextType>(
    () => ({
      lang,
      locale: lang === "ar" ? "ar-SA" : "en-US",
      setLang,
      dir: lang === "ar" ? "rtl" : "ltr",
      isArabic: lang === "ar",
      t,
    }),
    [lang, t],
  );

  return <I18nContext.Provider value={contextValue}>{children}</I18nContext.Provider>;
};

export const useI18n = () => useContext(I18nContext);

export const usePick = () => {
  const { lang } = useI18n();
  return useCallback(<T,>(arabic: T, english: T) => pickText(lang, arabic, english), [lang]);
};
